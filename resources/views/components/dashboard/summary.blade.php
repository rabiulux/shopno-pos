<script>
    fetchSummary();

    const formatCurrency = (value) => new Intl.NumberFormat('en-US', {
        style: 'currency',
        currency: 'USD'
    }).format(value);

    async function fetchSummary() {
        try {
            showLoader();
            const response = await axios.get('/summary');
            hideLoader();



            if (response.status === 200 && response.data.status === 'success') {

                const summary = response.data.summary;

                document.getElementById('product').innerText = summary.products;
                document.getElementById('category').innerText = summary.categories;
                document.getElementById('customer').innerText = summary.customers;
                document.getElementById('invoice').innerText = summary.invoices;

                document.getElementById('salesToday').innerText = formatCurrency(summary.sales_today);
                document.getElementById('salesThisMonth').innerText = formatCurrency(summary.sales_this_month);
                document.getElementById('total').innerText = formatCurrency(summary.total);
                document.getElementById('vat').innerText = formatCurrency(summary.vat);
                document.getElementById('payable').innerText = formatCurrency(summary.payable);

            } else {
                console.error('Failed to fetch summary:', response.data.message);
            }
        } catch (error) {
            hideLoader();
            console.error('Error fetching summary:', error);
        }
    }
</script>
